# Data type bool (boolean)
# When querying something in software,
# there are only two possible answers:
# yes (True) or no (False).
# There are several operators for "querying"
# Among them is ==, a logical operator that
# asks if one value equals another.
print(10 == 10)  # True => Yes
print(10 == 12)  # False => No
print(type(10 == 10))  # Returns the type of the value 'bool'
print(type(True))  # Returns the same as the above
