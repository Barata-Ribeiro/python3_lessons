"""
For + Range
range -> range(start, stop, step)
"""

numbers = range(10)  # ends at 10
# numbers = range(5, 10)  # starts at 5 and ends at 10
# numbers = range(2, 10, 2)  # starts at 2 and ends at 10 and step is 2

# In 'for' we don't need index
for number in numbers:
    print(number)
